package com.reviewclass.exercises;

public class ForDemo1 {

	public static void main(String[] args) {
		// When you know how many times you need the loop to occur (repetitions) it will
		// be a FOR LOOP
		// When you don’t know number of repetitions, you do a WHILE LOOP
		// Once you know type of loop, do you have an array or collection ? If yes use
		// ENHANCED FOR LOOP.
		// If you don’t have an array or collection, then use a FOR LOOP regular
		// for loops initialization condtion and increment decrement all happens at once
		// its cleaner code
		int x = 0;

		for (System.out.println("1 x=" + x); x < 5; System.out.println("3 x=" + x)) {
			System.out.println("  Batch 12 is Best ");

			x++;
		}
  
	}

}
